public class Vector {

	/**
	 * The vectors 2D coordinates
	 */
	public int x;
	public int y;

	/**
	 * Creates a new Vector
	 * 
	 * @param x coordinate
	 * @param y coordinate
	 */
	public Vector(int x, int y) {
		this.x = x;
		this.y = y;
	}
}